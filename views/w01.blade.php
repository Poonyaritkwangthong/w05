                <h1>Tamandua is a genus of anteaters in the Myrmecophagidae family with two species: the southern tamandua (T. tetradactyla) and the northern tamandua (T. mexicana).[1] They live in forests and grasslands, are semiarboreal, and possess partially prehensile tails.</h1>
                <p>They mainly eat ants and termites, but they occasionally eat bees, beetles, and insect larvae. In captivity, they will eat fruits and meat. They have no teeth and depend on their powerful gizzards to break down their food.</p>
                <p class="flow-root p-2">The genus name is derived from the word tamãdu'á in Tupi first recorded by Joseph of Anchieta in his Epistola quam plurimarum rerum naturalium quae S. Vicentii (nunc S. Pauli) provinciam incolunt sistens descriptionem (1560); ta- in tamãdu'á is deduced from taixi "ant" and the other half from mondé "to catch", mondá "thief" or monduár "hunter".</p>
                <p class="flow-root p-2">Tamanduas have tapered heads with a long, tubular snout, small eyes, and protruding ears. Their tapered mouths house a tongue reaching upwards of 40 cm (16 in) in length. The tail is hairless and pink in color, marked with an irregular pattern of black blotches. The forefeet possess four clawed digits, the third digit bearing the largest claw, while the hind feet have five digits.</p>
                <p class="flow-root p-2">Their fur is thick, bristly, yellowish-white to fawn in color, often with a broad black lateral band, covering nearly the whole of the side of their bodies. Northern tamanduas are marked with a black V on their backs and a "vest" over their torso; southern tamanduas vary in appearance across their range, from a vested pattern like northern tamandua, to only partially vested, to entirely light- or dark-colored.</p>
